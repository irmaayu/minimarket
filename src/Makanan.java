public class Makanan {
    int idProduk;
    String namaProduk;
    String jenisProduk;
    int stock;
    int harga;

    public Makanan(int idProduk, String namaProduk, String jenisProduk, int harga, int stock){
        this.idProduk = idProduk;
        this.namaProduk = namaProduk;
        this.jenisProduk = jenisProduk;
        this.stock = stock;
        this.harga = harga;
    }

}
